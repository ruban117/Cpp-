#include<iostream>
using namespace std;
int main(){
    int a;
    float b;
    double c;
    char d;
    bool e;
    long f;
    short g;
    cout<<"Size of Integer:- "<<sizeof(a)<<endl;
    cout<<"Size of Float:- "<<sizeof(b)<<endl;
    cout<<"Size of Double:- "<<sizeof(c)<<endl;
    cout<<"Size of Character:- "<<sizeof(d)<<endl;
    cout<<"Size of Boolean:- "<<sizeof(e)<<endl;
    cout<<"Size of Long Integer:- "<<sizeof(f)<<endl;
    cout<<"Size of Short Integer:- "<<sizeof(g)<<endl;
}